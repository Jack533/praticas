    public void listar() {

        tabela.setRowHeight(25);
        modelo.setNumRows(0);

        List<Midia> listaCompras = new ArrayList<Midia>();

        try {
            listaCompras = facade.listarCompraFutura();
        } catch (Exception ex) {
            Logger.getLogger(EditarMidia.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (!listaCompras.isEmpty()) {

            for (int i = 0; i < listaCompras.size(); i++) {
                Vector linha = new Vector();
                linha.add(listaCompras.get(i).getId());
                linha.add(listaCompras.get(i).getTituloPortugues());
                linha.add(listaCompras.get(i).getTipoDeMidia());
                modelo.addRow(linha);

            }

        }
    }

    public void removerCompraFutura() throws Exception {
        int linha = -1;
        linha = tabela.getSelectedRow();

        if (linha >= 0) {
            int id = (Integer) tabela.getValueAt(linha, 0);
            facade.removeCompraFutura(id);
            JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_REMOVER, "Mídia "));
            listar();
        } else {
            JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_SELECIONE, "uma Mídia "));
        }

    }

