    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int linha = -1;
        linha = tabela.getSelectedRow();

        if (linha >= 0) {
            int id = (Integer) tabela.getValueAt(linha, 0);
            try {
                facade.removeCompraFutura(id);
                facade.inserirColecao(id);
            } catch (Exception ex) {
                Logger.getLogger(ComprasFuturas.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_ADD_MIDIA, "a sua Coleção"));
            listar();
        } else {
            JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_SELECIONE, "uma Mídia "));
        }


    }//GEN-LAST:event_jButton2ActionPerformed

