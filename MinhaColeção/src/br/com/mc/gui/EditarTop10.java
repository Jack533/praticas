    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int pos, id = 0;

        if (recuperaIdTabelaSerie() == 0) {
            JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_ERRO_REMOVER_TOP10," uma Série "));
        } else {

            try {
                facade.removeTop10(recuperaIdTabelaSerie());
                JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_REMOVER_TOP10," Série "));
                listaTop10Serie = facade.listarTop10("Série");
                for (int i = 0; i < listaTop10Serie.size(); i++) {

                    if (listaTop10Serie.get(i).getPosicao() > recuperaPosTabelaSerie()) {
                        pos = listaTop10Serie.get(i).getPosicao();
                        id = listaTop10Serie.get(i).getIdMidia();
                        facade.editarTop10(pos - 1, id);

                    }

                }
                listarSerie();
            } catch (Exception ex) {
                Logger.getLogger(EditarTop10.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

