    public void listarFilme() {

        tabela1.setRowHeight(25);
        modelo1.setNumRows(0);
        String tipoMidia = "Filme";

        try {
            listaTop10 = facade.listarTop10(tipoMidia);
        } catch (Exception ex) {
            Logger.getLogger(EditarTop10.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (listaTop10 != null) {

            for (int i = 0; i < listaTop10.size(); i++) {
                Vector linha = new Vector();
                linha.add(listaTop10.get(i).getIdMidia());
                linha.add(listaTop10.get(i).getPosicao());
                linha.add(listaTop10.get(i).getTitulo());
                modelo1.addRow(linha);
            }

        }
    }

