    public int recuperaIdTabelaFilme() {

        int linha = -1;
        linha = tabela1.getSelectedRow();
        if (linha >= 0) {
            int id = (Integer) tabela1.getValueAt(linha, 0);
            return id;
        }
        return 0;

    }

