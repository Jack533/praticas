    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed

        posicao = (Integer) posicaoSelecionada.getValue();

        if (posicao != 0 && idMidia != 0) {

            int posAtual = 0;
            int idAtual = 0;

            if (tipoDeMidia.equals("Filme")) {
                if (facade.totalDeMidiasTop10("Filme")) {

                    if (!facade.estarRelacionadoTop10(idMidia)) {

                        for (int i = 0; i < listaTop10.size(); i++) {

                            if (listaTop10.get(i).getPosicao() >= posicao) {
                                posAtual = listaTop10.get(i).getPosicao();
                                idAtual = listaTop10.get(i).getIdMidia();
                                facade.editarTop10(posAtual + 1, idAtual);

                            }

                        }

                        try {
                            facade.inserirTop10(idMidia, posicao);
                            JOptionPane.showMessageDialog(null,  MensagensUtil.getValor(MensagensUtil.MSG_ADD_TOP10," Filme "));
                            listarFilme();
                        } catch (Exception ex) {
                            Logger.getLogger(EditarTop10.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    } else {
                        JOptionPane.showMessageDialog(null,  MensagensUtil.getValor(MensagensUtil.MSG_ERRO_ADD_TOP10," O Filme" ));
                    }

                } else {
                    JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_TOP10_COMPLETO, " de Filme "));
                }

            } else {
                if (facade.totalDeMidiasTop10("Série")) {

                    if (!facade.estarRelacionadoTop10(idMidia)) {

                        for (int i = 0; i < listaTop10Serie.size(); i++) {

                            if (listaTop10Serie.get(i).getPosicao() >= posicao) {
                                posAtual = listaTop10Serie.get(i).getPosicao();
                                idAtual = listaTop10Serie.get(i).getIdMidia();
                                facade.editarTop10(posAtual + 1, idAtual);

                            }

                        }

                        try {
                            facade.inserirTop10(idMidia, posicao);
                            JOptionPane.showMessageDialog(null,  MensagensUtil.getValor(MensagensUtil.MSG_ADD_TOP10," A Série "));
                            listarSerie();
                        } catch (Exception ex) {
                            Logger.getLogger(EditarTop10.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_ERRO_ADD_TOP10, " A Série"));
                    }

                } else {
                    JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_TOP10_COMPLETO, " de Série "));
                }
            }

            titulo.setText("");
            tipoDeMidiaSelecionada.setSelectedItem(null);
            posicaoSelecionada.setValue(0);

        } else {
            JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_ERRO_TOP10));
        }


    }//GEN-LAST:event_jToggleButton3ActionPerformed

