    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Artista art = new Artista();

        art.setNome(nome.getText());
        art.setSexo(String.valueOf(jComboBoxSex.getSelectedItem()));
        art.setProfissao(String.valueOf(jComboBoxFuncao.getSelectedItem()));
        art.setNacionalidade(nacionalidade.getText());

        IFacade facade = DaoFactory.createFacade();

        try {
            facade.inserirArtisma(art);
            JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_SUCESSO_SALVAR, " Artista "));
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(CadastrarEmpresa.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_ERRO_SALVAR, " Artista "));
        }

    }//GEN-LAST:event_jButton2ActionPerformed

